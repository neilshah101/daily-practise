first_no = float(input("enter the first number:  "))
operation = input("enter the desire operation: ")
second_no = float(input("enter the second number:  "))


if operation == "+":
    total= first_no + second_no
    print(total) 

elif operation == "-":
    total= first_no - second_no
    print(total)

elif operation == "*":
    total = first_no * second_no
    print(total)

elif operation == "/":
    total = first_no / second_no
    print(total)



     